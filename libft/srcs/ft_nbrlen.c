#include "libft.h"

size_t	ft_nbrlen(long long nbr)
{
	size_t	len;

	len = (nbr < 0) ? 2 : 1;
	while (ABS(nbr / 10) > 0)
	{
		len++;
		nbr = nbr / 10;
	}
	return (len);
}
